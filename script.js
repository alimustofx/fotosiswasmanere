// search by NIS
function searchPhoto() {
    const identityNumber = document.getElementById('identity-number').value;
    const resultDiv = document.getElementById('result');

    // check input
    if (isNaN(identityNumber) || identityNumber.trim() === "") {
        showPopup("Anda harus memasukkan nomor.");
        document.getElementById('identity-number').value = '';
        return;
    }
    
    // check database
    if (identityNumber in mockDatabase) {
        const photoUrl = mockDatabase[identityNumber];
        if (photoUrl) {
            resultDiv.innerHTML = `<p>Foto kamu bisa ditemukan <a href="${photoUrl}" target="_blank" id="photo-link">disini</a>.</p>`;
            
            // clear input after link open
            document.getElementById('photo-link').addEventListener('click', () => {
                resultDiv.innerHTML = '';
            });
        } else {
            showPopup("Foto tidak tersedia untuk NIS ini.");
        }
    } else {
        showPopup("NIS Anda tidak ditemukan, cek kembali.");
    }
    
    // clear input
    document.getElementById('identity-number').value = '';
}

// show pop up
